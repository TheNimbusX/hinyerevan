export function translateKey(key, language = currentLanguage.value) {
  const dictionary = messages[language] || messages.hy
  let text = dictionary[key] || messages.hy[key] || key
  return text
}

export function setLanguage(language) {
  if (currentLanguage.value === language) return

  currentLanguage.value = language
  localStorage.setItem(STORAGE_KEY, language)
  document.documentElement.lang = language
  clearApiCache()
  window.dispatchEvent(new CustomEvent('hinyerevan:language-changed', { detail: { language } }))
}

export function useI18n() {
  const dictionary = computed(() => messages[currentLanguage.value] || messages.hy)

  return {
    currentLanguage,
    languages,
    setLanguage,
    t: (key, vars) => {
      let text = dictionary.value[key] || key
      if (vars) {
        for (const [name, value] of Object.entries(vars)) {
          text = text.replaceAll(`{${name}}`, String(value))
        }
      }
      return text
    },
  }
}
